package com;

public class TryCatchAndFinally {

	public static void main(String[] args) {
		try {
			int res = 10/0;
			System.out.println("No Exception");
		} catch (Exception e) {
			System.out.println("Exception "+e);
		}finally {
			System.out.println("Finally block");
		}
		System.out.println("Normal Statement");
	}

}
